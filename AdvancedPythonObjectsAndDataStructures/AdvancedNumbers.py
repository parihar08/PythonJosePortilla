print('********Hexadecimal*************','\n')

#Hexadecimal
print(hex(246))     #0xf6
print(hex(512))     #0x200

print('********Binary*************','\n')

#Binary
print(bin(128))     #0b10000000
print(bin(512))     #0b1000000000

print('*********Power************','\n')

#Power
print(pow(2,4))     #16
print(pow(2,4,3))   #1 (2**4)%3

print('*********Absolute************','\n')

#Absolute
print(abs(2))       #2

print('*********Round************','\n')

#Round
print(round(3.1))           #3.0
print(round(3.9))           #4.0
print(round(3.141592,2))    #3.14

